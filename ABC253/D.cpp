ll gcd(ll a, ll b)
{
   if (a%b == 0)
   {
       return(b);
   }
   else
   {
       return(gcd(b, a%b));
   }
}

ll lcm(ll a, ll b)
{
   return a / gcd(a, b) * b;
}

int main(){
  ll n,a,b;

  cin >> n >> a >> b;

  ll c = lcm(a,b);

  ll ans = n * (n+1)/2;
  ans -= (n/ a) * (2 * a + ((n/a)-1)*a) / 2;
  ans -= (n/ b) * (2 * b + ((n/b)-1)*b) / 2;
  ans += (n/ c) * (2 * c + ((n/c)-1)*c) / 2;
  cout << ans << endl;


}

