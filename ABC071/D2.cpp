#define MOD 1000000007

typedef long long int ll;

const ll INF=(ll)1e18;
//const int INF=(1<<30);

const int mod = 1000000007;
struct mint {
  ll x; // typedef long long ll;
  mint(ll x=0):x((x%mod+mod)%mod){}
  mint operator-() const { return mint(-x);}
  mint& operator+=(const mint a) {
    if ((x += a.x) >= mod) x -= mod;
    return *this;
  }
  mint& operator-=(const mint a) {
    if ((x += mod-a.x) >= mod) x -= mod;
    return *this;
  }
  mint& operator*=(const mint a) {
    (x *= a.x) %= mod;
    return *this;
  }
  mint operator+(const mint a) const {
    mint res(*this);
    return res+=a;
  }
  mint operator-(const mint a) const {
    mint res(*this);
    return res-=a;
  }
  mint operator*(const mint a) const {
    mint res(*this);
    return res*=a;
  }
  mint pow(ll t) const {
    if (!t) return 1;
    mint a = pow(t>>1);
    a *= a;
    if (t&1) a *= *this;
    return a;
  }

  // for prime mod
  mint inv() const {
    return pow(mod-2);
  }
  mint& operator/=(const mint a) {
    return (*this) *= a.inv();
  }
  mint operator/(const mint a) const {
    mint res(*this);
    return res/=a;
  }
};

int main(){
  int N;
  cin >> N;
  string s1,s2;
  cin >> s1 >>s2;

  mint ans;
  int i;
  int t;
  if(s1[0]==s2[0]){
    ans = 3;
    i = 1;
    t = 0;
  }else{
    ans = 6;
    i = 2;
    t = 1;
  }

  while(i<N){
    if(s1[i]==s2[i]){
      if(t==0)ans*=2;
      i++;
      t = 0;
    }else{
      if(t==0)ans *= 2;
      else ans *= 3;
      i+=2;
      t = 1;
    }
  }

  cout << ans.x << endl;
}


